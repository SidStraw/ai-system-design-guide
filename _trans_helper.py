w('## 經驗教訓\n')
w('\n')
w('在生產環境中實施完整 eval 流程的真實教訓：\n')
w('\n')
w('**關於建立 Judge（Chapters 4, 10）**\n')
w('\n')
w('1. **LLM-as-Judge 強大但需要護欄** - 沒有適當驗證，judge 可以自信地給出錯誤答案。始終對基準真值進行驗證。\n')
w('\n')
w('2. **你必須對基準真值測試評測器** - TNR=22% 的 judge 看起來合理但實際上有害——它漏掉了大多數真實失敗。\n')
w('\n')
w('3. **Train/Dev/Test 分割帶來信心** - 沒有它們，你在欺騙自己關於 judge 品質的問題。這是不可缺少的。\n')
w('\n')
w('4. **迭代 judge prompt 至關重要** - 第一個 prompt 永遠不夠好。計劃至少 3-5 次迭代。詳見附錄 E。\n')
w('\n')
w('5. **解釋先於判決是第一技術** - 要求 judge 在標注前推理比任何其他單一更改更能提高準確性。\n')
w('\n')
w('**關於流程與方法論（Chapters 3, 11, 12）**\n')
w('\n')
w('6. **錯誤分析才是真正的工作** - 精美的工具不重要，如果你沒有坐下來審閱你的失敗。開放編碼→軸向編碼→優先排序是有效的工作流程。\n')
w('\n')
w("7. **人工標注者的分歧比你想像的更多** - 在信任你的基準真值之前量測標注者間一致性（Cohen's Kappa）。如果人類不能達成一致，judge 也不會。\n")
w('\n')
w('8. **閉環是優秀團隊與卓越團隊的分水嶺** - 執行 eval 只是工作的一半。另一半是系統性地將失敗轉化為改進並防止回歸。\n')
w('\n')
w('**關於生產與擴展（Chapters 9, 13）**\n')
w('\n')
w('9. **安全 eval 不是可選的** - Prompt 注入、PII 洩漏和越獄偵測應該在你擔心品質 eval 之前就運行。\n')
w('\n')
w('10. **先昂貴，再優化** - 使用 GPT-4o/Claude Sonnet 建立你的效能上限，然後測試更便宜的模型是否能匹配它。通常可以。\n')
w('\n')
w('11. **抽樣優於窮舉評測** - 用統計嚴謹性評測 10% 的追蹤記錄比用糟糕的 judge 評測 100% 給出更好的答案。\n')
w('\n')
w('12. **好的可觀測性工具使工作流程快 10 倍** - 在一個平台中整合追蹤記錄、評測、資料集和實驗（Phoenix、Langfuse 等）比拼湊自訂腳本節省大量時間。\n')
