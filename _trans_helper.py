w('<a id="evaluation--metrics-terms"></a>\n')
w('### 評測與指標術語\n')
w('\n')
w('| 術語 | 定義 |\n')
w('|------|--------|\n')
w('| **Eval（評測）** | 檢查 AI 系統是否對特定標準正確工作的系統性測試 |\n')
w('| **LLM-as-a-Judge** | 使用語言模型自動評估另一個 AI 系統的輸出 |\n')
w('| **基準真值（Ground Truth）** | 人工驗證的標籤，代表「正確」答案；用於量測 judge 準確性 |\n')
w('| **真正例率（TPR）** | judge 正確識別的實際正例（如好的回應）百分比。也稱為*召回率*或*敏感性*。公式：TP / (TP + FN) |\n')
w('| **真負例率（TNR）** | judge 正確捕獲的實際負例（如壞的回應）百分比。也稱為*特異性*。公式：TN / (TN + FP) |\n')
w('| **誤報（FP）** | judge 說「通過」但真實答案是「失敗」——漏掉的缺陷 |\n')
w('| **漏報（FN）** | judge 說「失敗」但真實答案是「通過」——虛假警報 |\n')
w('| **精確率（Precision）** | 在 judge 標注為正例的所有項目中，實際為正例的比例。公式：TP / (TP + FP) |\n')
w('| **F1 分數** | 精確率和召回率的調和平均數——平衡兩者的單一數字。公式：2 * (Precision * Recall) / (Precision + Recall) |\n')
w('| **混淆矩陣** | 顯示 TP、FP、FN、TN 計數的 2x2 表格——所有分類指標的基礎 |\n')
w('| **信賴區間（CI）** | 真實指標可能落在其中的值範圍（例如，72%-81%），考慮到抽樣不確定性 |\n')
w('| **偏差修正** | 調整原始 judge 分數以考慮系統性過度或低估通過/失敗 |\n')
w("| **Cohen's Kappa** | 量測兩個評分者（或評分者與基準真值）之間一致性的統計數，調整了偶然一致性。值：<0.2 差，0.4-0.6 中等，0.6-0.8 顯著，>0.8 幾乎完美 |\n")
w('\n')
w('<a id="data--workflow-terms"></a>\n')
w('### 資料與工作流程術語\n')
w('\n')
w('| 術語 | 定義 |\n')
w('|------|--------|\n')
