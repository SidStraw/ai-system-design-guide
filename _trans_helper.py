w('<a id="conclusion"></a>\n')
w('## 結論\n')
w('\n')
w('AI eval 不只是「測試」——它是一種觸及工程、產品管理和品質保證的**產品開發方法論**。\n')
w('\n')
w('**關鍵要點：**\n')
w('\n')
w('1. **每個人都需要 eval** — 不只是大公司。如果你的 AI 應用接觸使用者，你需要系統性評測。\n')
w('2. **從錯誤分析開始** — 在建立任何自動化之前，坐下來審閱你的失敗（Chapter 3）。\n')
w('3. **PM 和 QA 必須主導** — 錯誤分析和標準定義是產品/品質工作，不只是工程任務。\n')
w('4. **逐步建立** — 從程式碼型 eval 開始，然後添加 LLM judge，然後添加安全 eval。不要試圖一次完成所有事情。\n')
w('5. **量測重要的事情** — 特定應用的標準，而不是通用的「有幫助性」分數。\n')
w('6. **TPR 和 TNR 都重要** — 捕獲失敗但也誤報的 judge 是有害的。兩者都要量測。\n')
w('7. **分割你的資料** — Train/Dev/Test 是必須的。沒有它，你就在過度擬合你的 judge。\n')
w('8. **修正偏差** — 使用統計修正（Chapter 10）獲得誠實的指標。\n')
w('9. **閉環** — 不能帶來改進的 eval 是浪費的努力（Chapter 11）。\n')
w('10. **為規模計劃** — 從最好的模型開始，然後為成本優化（Chapter 13）。\n')
w('\n')
w('**你的行動計劃（詳見附錄 G）：**\n')
w('\n')
w('1. 第 1 週：設置可觀測性（Phoenix、Langfuse 或你選擇的工具），進行錯誤分析\n')
w('2. 第 2 週：建立 2-3 個核心程式碼型 eval\n')
w('3. 第 3 週：建立並驗證帶有適當 train/dev/test 分割的 LLM judge\n')
w('4. 第 4 週：進階主題——RAG eval、多輪 eval、安全 eval、自動化\n')
w('5. 持續進行：每週 30 分鐘維護 + 回歸測試\n')
w('\n')
w('**記住：** 發布最佳 AI 產品的團隊，是那些擁有最佳 eval 的團隊。不是最先進的模型。不是最大的團隊。是那些系統性量測和改進的團隊。\n')
w('\n')
w('今天就開始。你未來的自己會感謝你。\n')
